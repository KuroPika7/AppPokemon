package com.si61.apppokemon.Model;

public class ModelPokemon {
    private String id, name, type, ability, height, weight;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getAbility() {
        return ability;
    }

    public String getHeight() {
        return height;
    }

    public String getWeight() {
        return weight;
    }
}
